/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func flatten(root *TreeNode)  {
	var stack []*TreeNode
	var prev *TreeNode
	curr := root

	for curr != nil || len(stack) > 0 {
		if curr == nil {
			curr = stack[len(stack) - 1]
			stack = stack[:len(stack) - 1]
		}

		if prev != nil {
			prev.Left = nil
			prev.Right = curr
		}

		if curr.Right != nil {
			stack = append(stack, curr.Right)
		}

		prev = curr
		curr = curr.Left
	}
}
