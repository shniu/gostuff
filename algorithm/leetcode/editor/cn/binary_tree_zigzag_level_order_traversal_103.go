/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func zigzagLevelOrder(root *TreeNode) [][]int {
	if root == nil {
		return [][]int{}
	}

	var res [][]int

	var fromLeft = true
	var queue []*TreeNode
	queue = append(queue, root)

	for len(queue) > 0 {
		level := len(res)
		res = append(res, []int{})
		size := len(queue)

		for i := 0; i < size; i++ {
			curr := queue[i]
			queue[i] = nil

			res[level] = append(res[level], curr.Val)

			if curr.Left != nil {
				queue = append(queue, curr.Left)
			}
			if curr.Right != nil {
				queue = append(queue, curr.Right)
			}
		}

		if !fromLeft {
			res[level] = reverseInts(res[level])
		}

		queue = queue[size:]
		fromLeft = !fromLeft
	}

	return res
}

func reverseInts(input []int) []int {
	if len(input) == 0 {
		return input
	}
	return append(reverseInts(input[1:]), input[0])
}
